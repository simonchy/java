package bankproject;

public class Forloop {
    public static void main(String[] args) {


        // outer loop prints weeks
        for (int i = 0; i <= 3; ++i) {
            System.out.println("Avocado");
            // inner loop prints days
            for (int j = 0; j <2; ++j) {
                System.out.println("Apple");

            }
        }
    }
}










